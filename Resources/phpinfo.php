PHP has many environmental variables that you are able to update as needed. 
For example, you may need to update your php memory_limit to prevent certain scripts from running out of memory. 
In order to change these values, you must first be able to see what they are. 
To do that, you will need to create a phpinfo page. 
A phpinfo page shows you all of your php environment settings.

A phpinfo page is simply a php page with the following code:

<? phpinfo(); ?>
